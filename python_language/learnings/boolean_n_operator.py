'''

<   less than
>   greater than
==  equals to
!=  not equals to
>=  greater than or equals to
<= less than or equals to

'''

number = 5

print("Comperision Operator")
print("1.", number < 5)
print("2.", number > 5)
print("3.", number == 5)
print("4.", number != 5)
print("5.", number >= 5)
print("6.", number <= 5)
print('\n\n')


'''
LOGICAL OPERATOR

and     True if both operands are true
or      True if one of two operands are true
not     True if the operand is false

'''

age = 22
gpa = 5.5
result = True

print("LOGICAL OPERATOR")
print("1.", age >= 18 and gpa >= 7)
print("2.", age >= 18 or gpa >= 7)
print("3.", not result)
print("4.", not age < 23)
