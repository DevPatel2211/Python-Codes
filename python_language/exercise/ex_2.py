# convert kilometer into miles ---> 1 km = 0.621371 miles


kilometer = float(input("Enter the Kilometer(to convert into miles) :"))
kms_to_miles = kilometer * 0.621371
print("Miles =", kms_to_miles)


mile = float(input("Enter the miles(to conveert into miles) :"))
miles_to_kms = mile / 0.621371
print("Kilometers =", miles_to_kms)
